     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($trs_code)
    {
        $data['detailTrs'] = Transaction::all()->where('trs_code', $trs_code);;
        $data['totalPrice'] = 0;
        $data['trs_code'] = $trs_code;
        foreach ($data['detailTrs'] as $detail) {   
            $data['totalPrice'] +=  $detail->product->price * $detail->qty;
        }
        return view('transaction.show', $data);
    }

